package java_program;

public class SwitchCaseClass {

	public static void main(String[] args) {
		
		int a =13;
		int b =15;
		int c =(b-a);
		
		switch(c) {
		
		case 1:
			System.out.println("c value is 1");
		break;
		
		case 2:
			System.out.println("c value is 2");
		break;
		
		case 3:
			System.out.println("c value is 3");
		break;
		
		case 4:
			System.out.println("c value is 4");
		break;
		
		case 5:
			System.out.println("c value is 5");
		break;
		
		default:
			System.out.println("c value is Out of the Range");
				
		
		}
	}

}
