package java_programs;

public class DataType {

	public static void main(String[] args) {
		
		byte b = 20 ; //1 byte, 8bit, 0
		System.out.println(b);
		
		short s =32767; //2 byte, 16bit, 0
		
		int i = 200000; //4 Byte, 32 bit, 0
		
		long l1 = 23456098477667799L; //8 byte, 64 bit, 0
		
		System.out.println(l1);
		
		double d = 20.56 ; // 8 byte, 64 bit 0.0
		System.out.println(d);
		
		float f = 12.5f; //4byte, 32 bit, 0.0;
		
		char c= 'z'; //2 Byte, 16 bit
		
		boolean val = 20!=15 ; //1 byte, false
		
		System.out.println(val);
		
		System.out.println(c);
	}

}
