package java_programs;

public class DataType {

	public static void main(String[] args) {
		
		byte a = 0 ; // 1 byte, 8 bit, 0
		System.out.println(a);
		
		short s = 32767; //2 Byte, 16 bit, 0
		
		int i = 2000000000; //4 Byte, 32 bit, 0
		
		long l1 = 20000345006565656L; //8 BYTE, 64 Bit, 0
		
		System.out.println(l1);
		
	    double d1 = 123.567; //8 byte, 64 bit 0.0;
	    
	    float f1 = 20.50f; //4 byte, 32 bit, 0.0 
	    
	    char c = 'v'; //2 Byte, 16 bit
		
	    boolean result = 20<=5 ; //1 bit, false
	    
	    System.out.println(result);
	    
		
	}

}
